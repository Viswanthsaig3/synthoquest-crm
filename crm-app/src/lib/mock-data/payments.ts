export function getPaymentsByStudent(studentId: string): Payment[] {
  return mockPayments.filter(payment => payment.studentId === studentId)
}

export function getPaymentsByEnrollment(enrollmentId: string): Payment[] {
  return mockPayments.filter(payment => payment.enrollmentId === enrollmentId)
}

export function getInvoiceByStudent(studentId: string): Invoice[] {
  return mockInvoices.filter(invoice => invoice.studentId === studentId)
}

export function getPendingPayments(): Payment[] {
  return mockPayments.filter(payment => payment.status === 'pending' || payment.status === 'partial')
}

export function getTotalRevenue(): number {
  return mockPayments
    .filter(p => p.status === 'paid')
    .reduce((sum, p) => sum + p.amount, 0)
}

export function getRevenueByMonth(month: number, year: number): number {
  return mockPayments
    .filter(p => {
      if (p.status !== 'paid' || !p.paidAt) return false
      const date = new Date(p.paidAt)
      return date.getMonth() === month && date.getFullYear() === year
    })
    .reduce((sum, p) => sum + p.amount, 0)
}