const createInvoiceItems = (courseName: string, fee: number, discount: number): InvoiceItem[] => {
  const items: InvoiceItem[] = [
    {
      id: generateId(),
      description: `${courseName} - Course Fee`,
      quantity: 1,
      unitPrice: fee,
      total: fee,
    },
  ]
  if (discount > 0) {
    items.push({
      id: generateId(),
      description: 'Discount',
      quantity: 1,
      unitPrice: -discount,
      total: -discount,
    })
  }
  return items
}

