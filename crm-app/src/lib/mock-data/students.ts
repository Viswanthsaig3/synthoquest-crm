import { Student, Enrollment } from '@/types/student'

const createEnrollment = (
  id: string,
  studentId: string,
  courseName: string,
  batchId: string,
  batchName: string,
  courseFee: number,
  paidAmount: number,
  status: Enrollment['status'],
  progress: number,
  instructor: string
): Enrollment => ({
  id,
  studentId,
  courseId: courseName.toLowerCase().replace(/\s+/g, '_'),
  courseName,
  batchId,
  batchName,
  status,
  enrolledAt: new Date(Date.now() - Math.random() * 90 * 24 * 60 * 60 * 1000),
  enrollmentFee: 5000,
  courseFee,
  discount: Math.random() > 0.7 ? 5000 : 0,
  totalFee: courseFee - (Math.random() > 0.7 ? 5000 : 0),
  paidAmount,
  dueAmount: courseFee - paidAmount - (Math.random() > 0.7 ? 5000 : 0),
  paymentPlan: Math.random() > 0.5 ? 'full' : 'installment',
  startDate: new Date(Date.now() - Math.random() * 30 * 24 * 60 * 60 * 1000),
  expectedEndDate: new Date(Date.now() + 60 * 24 * 60 * 60 * 1000),
  completedDate: status === 'completed' ? new Date() : undefined,
  instructor,
  progress,
  certificateIssued: status === 'completed',
  certificateId: status === 'completed' ? `cert_${id}` : undefined,
})

