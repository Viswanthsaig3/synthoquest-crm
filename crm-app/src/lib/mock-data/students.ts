export function getStudentById(id: string): Student | undefined {
  return mockStudents.find(student => student.id === id)
}

export function getStudentsByStatus(status: string): Student[] {
  return mockStudents.filter(student => student.status === status)
}

export function getStudentsByCourse(course: string): Student[] {
  return mockStudents.filter(student => 
    student.enrollments.some(e => e.courseName === course)
  )
}

export function getActiveStudents(): Student[] {
  return mockStudents.filter(s => s.status === 'active')
}

export function getStudentsWithDuePayments(): Student[] {
  return mockStudents.filter(s => s.totalDue > 0)
}